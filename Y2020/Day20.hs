data EdgeDirection = Normal | Reversed deriving (Show,Eq)
data Transform = Id | Rot90 | Rot180 | Rot270 | FlipX | FlipY | Diag1 | Diag2 deriving (Show,Eq,Enum,Bounded)

-- Tile and Image
type Pixel = Char
